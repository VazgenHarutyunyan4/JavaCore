package classwork;

public class Calculator {
    double plus(double x, double y) {
        return x + y;

    }


    double minus(double x, double y) {
        return x - y;
    }

    double multiply(double x, double y) {
        return x * y;
    }

    double divide(double x, double y) {
        return x / y;
    }
}
