package classwork;

public class Calculator {
    public double plus(double x, double y) {
        return x + y;

    }


    public double minus(double x, double y) {
        return x - y;
    }

    public double multiply(double x, double y) {
        return x * y;
    }

    public double divide(double x, double y) {
        return x / y;
    }
}
